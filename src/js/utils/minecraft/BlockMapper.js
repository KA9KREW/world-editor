// Find a HYTOPIA block by ID
export function getHytopiaBlockById(id) {
  return DEFAULT_HYTOPIA_BLOCKS.find(block => block.id === id);
}

// Generate a new unique block ID for custom blocks
export function generateUniqueBlockId(existingBlocks) {
  const maxId = Math.max(...existingBlocks.map(block => block.id), 0);
  return maxId + 1;
} 